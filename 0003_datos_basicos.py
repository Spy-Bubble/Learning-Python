# Los 4 tipos de datos básicos en Python son:
# Números (int, float)
# Cadenas de texto (str)
# Booleanos (bool)

# 1. int (enteros)
# Se utiliza para almacenar números enteros: positivos, negativos y 0 (sin decimales) 
# Numero positivo
entero_positivo = 42
# Numero negativo
entero_negativo = -42  
# Cero
cero = 0

# 2. float (decimales)
# Se utiliza para almacenar números con decimales (números reales)  
# Numero positivo con decimales
decimal_positivo = 3.14 
# Numero negativo con decimales
decimal_negativo = -3.14
# Cero con decimales
cero_decimal = 0.0
# Notación científica
notacion_cientifica = 1.5e3  # Equivale a 1500.0
notacion_cientifica_negativa = 1.5e-3  # Equivale a 0.0015  

# 3. str (cadenas de texto)
# Se utiliza para almacenar texto (una secuencia de caracteres) 
# Se pueden usar comillas simples (' ') o dobles (" ")
cadena_simple = 'Hola, mundo!'         # Comillas simples
cadena_doble = "Hola, mundo!"          # Comillas dobles

# 4. bool (booleanos)
# Se utiliza para almacenar valores de verdad: True (verdadero) o False (falso)
es_verdadero = True
es_falso = False
# Los booleanos son útiles para controlar el flujo del programa (condiciones, bucles, etc.)
# También se pueden obtener como resultado de comparaciones
comparacion_igual = (5 == 5)      # True    
comparacion_iual_no = (5 == 3)   # False