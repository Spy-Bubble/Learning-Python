# Los valores literales son valores fijos que se escriben directamente en el código fuente:

# Estos incluyen números, cadenas de texto, booleanos y otros tipos de datos básicos.   
frase = "Hola, mundo!"  # Literal de cadena
numero = 42             # Literal entero
pi = 3.14               # Literal de punto flotante
es_valido = True        # Literal booleano
# Los literales se utilizan para asignar valores a variables, pasar argumentos a funciones y en expresiones.

# Para crear valores no literales, podemos hacer uso de funciones, operaciones y estructuras de datos:
# Con la función input(), podemos obtener un valor no literal del usuario:
entrada_usuario = input("Ingresa tu nombre: ")  # Valor no literal
# Aquí, 'entrada_usuario' no es un literal, ya que su valor depende de la entrada del usuario en tiempo de ejecución.

# También podemos realizar operaciones para generar valores no literales:
suma = numero + 10  # Valor no literal (resultado de una operación) 
# Aquí, 'numero' es un literal entero, pero 'suma' es un valor no literal resultante de una operación.

# Además, podemos crear listas o diccionarios que contengan valores no literales:
lista = [frase, entrada_usuario, suma]  # Lista con valores no literales
# En este caso, 'lista' contiene tanto literales como valores no literales.

# En resumen, los literales son valores fijos escritos directamente en el código, 
# mientras que los valores no literales se generan a través de operaciones, funciones o interacciones con el usuario.